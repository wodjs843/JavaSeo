	package Java0511;

		public class ex04_arithmesicOperation {
			
			public static void main(String[] args) {
				
				
				// 정수형 변수를 2개 선언
				// 덧셈 뺄셈 곱셈 나누기 퍼센티지 연산 수행
				
				int num1 = 7;
				int num2 = 2;
				int result;
				
				// 덧셈
				
				result = num1 + num2;
				System.out.println("+ 결과 : " + result);
				
				// 뺄셈 (-)
				// 곱셈 (x)
				// 나눗셈 (/)
				// 나머지 (%)
				
				
				//뺄셈
				result = num1 - num2;
				System.out.println("+결과 : " + result);
				
				//나눗셈
				result = num1 / num2;
				System.out.println(" / 결과 : " +result);
				
				//곱셈
				result = num1 * num2;
				System.out.println("* 결과 : " +result );
				
				//퍼센티지
				result = num1 % num2;
				System.out.println("%결과 : " + result);
				
				//덧셈
				result = num1 + num2;
				System.out.println("+ 결과 : " + result);
				
				
				
				
				
				
				
			}

}
